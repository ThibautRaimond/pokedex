import "./toggleTheme.css";

const ToggleTheme = ({ onChange, state, icons }) => {
  const handleKeyDown = (e) => {
    if (e.key === "Enter" || e.key === " ") {
      e.preventDefault();
      onChange();
    }
  };

  // texte du bouton
  const actionLabel =
    state === "dark" ? "Désactiver le thème sombre" : "Activer le thème sombre";
  // texte à vocaliser après changement
  const liveText =
    state === "dark" ? "Thème sombre activé" : "Thème sombre désactivé";

  return (
    <div className="toggleContainer">
      {/* texte invisible pour vocaliser le changement d'état */}
      <div aria-live="polite" className="srOnly">
        {liveText}
      </div>
      <button
        className="toggleButton"
        aria-label={actionLabel}
        onClick={onChange}
        onKeyDown={handleKeyDown}
      >
        <div className="toggleWrapper">
          <div
            id="toggleSwitch"
            className={`darkModeInput ${state === "dark" ? "inputChecked" : ""}`}
            checked={state === "dark"}
            readOnly
          />
          <div
            className="toggleLabel"
            aria-hidden="true"
          >
            <span className="toggleThumb"></span>
            {/* Étoiles visibles en mode dark (avec la lune) */}
            {state === "dark" && (
              <>
                <div className="leftIcon star1" aria-hidden="true"></div>
                <div className="leftIcon star2" aria-hidden="true"></div>
                <div className="leftIcon star3" aria-hidden="true"></div>
              </>
            )}
            {/* Nuage visible en mode clair (avec le soleil) */}
            {state === "light" && (
              <svg
                viewBox="0 0 16 16"
                className="rightIcon cloud"
                aria-hidden="true"
              >
                <path
                  transform="matrix(.77976 0 0 .78395-299.99-418.63)"
                  fill="#f1f1f1ea"
                  d="m391.84 540.91c-.421-.329-.949-.524-1.523-.524-1.351 0-2.451 1.084-2.485 2.435-1.395.526-2.388 1.88-2.388 3.466 0 1.874 1.385 3.423 3.182 3.667v.034h12.73v-.006c1.775-.104 3.182-1.584 3.182-3.395 0-1.747-1.309-3.186-2.994-3.379.007-.106.011-.214.011-.322 0-2.707-2.271-4.901-5.072-4.901-2.073 0-3.856 1.202-4.643 2.925"
                ></path>
              </svg>
            )}
          </div>
        </div>
        
        {/* Texte visible à côté du toggle */}
        <span className="toggleText">{actionLabel}</span>
      </button>
    </div>
  );
};

export default ToggleTheme;
